function datosArtista1(){

    var nombre = prompt("Escribe el nombre")
    var divMensajeNombre = document.getElementById('nombre1')
    divMensajeNombre.innerText = nombre
  
    var edad = prompt("Escribe la edad")
    var divMensajeEdad = document.getElementById('edad1')
    divMensajeEdad.innerText = edad

    var ciudad = prompt("Escribe la ciudad")
    var divMensajeCiudad = document.getElementById('ciudad1')
    divMensajeCiudad.innerText = ciudad

    var generoMusical = prompt("Escribe el género musical")
    var divMensajeGeneroMusical = document.getElementById('genero1')
    divMensajeGeneroMusical.innerText = generoMusical 

}

